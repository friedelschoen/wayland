	fmt.Fprintf(w, "c = make(chan runtime.Event)\n")
	fmt.Fprintf(w, "i.waitfor[\"%s\"] = c\n", eventName)
	fmt.Fprintf(w, "}\n")
	fmt.Fprintf(w, "return (<-c).(%s%sEvent)\n", ifaceName, eventName)
