	// fmt.Fprintf(w, "res := make(chan %s%sEvent)\n", ifaceName, eventName)
	// fmt.Fprintf(w, "i.%sHandler = func(evt %s%sEvent) {\n", eventNameLower, ifaceName, eventName)
